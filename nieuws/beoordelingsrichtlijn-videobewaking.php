                                <p><strong>Kiwa maakt bekend dat nu versie 3 van de Beoordelingsrichtlijn K21039 Videobewakingssystemen van kracht is. In deze nieuwe versie van het schema 
                                    zijn diverse wijzigingen doorgevoerd die verband houden met wijzigingen in de regelgeving voor cameratoezicht.</strong></p>
                                <p>De nieuwe versie is het gevolg van wijzigingen in de RPBR (Regeling particuliere beveiligingsorganisaties en recherchebureaus). Op 19 december 
