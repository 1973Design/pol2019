                                    opgesteld. Naast de wetswijziging heeft de internationale norm EN-IEC 62676-4 een belangrijke rol gekregen in de BRL Videobewakingssystemen.
                                </p>
                                <h3>Veranderd</h3>
                                <p>Wat is er zoal veranderd? De naam van de BRL K21039 is gewijzigd van Videosystemen naar Videobewakingssystemen. De EN-IEC 62676-4 heeft een 
                                    belangrijke rol gekregen. Deze norm geeft handvaten aan het ontwerp, installeren, opleveren en onderhouden van videosystemen. In de BRL 
                                    Videobewakingssystemen zijn diverse verwijzingen opgenomen naar deze norm. In de voorgaande versie was het nog mogelijk om te kiezen tussen 
                                    scope A en B. Door de wetswijziging is dit niet meer mogelijk. Verder dient gediplomeerd personeel in dienst te zijn voor het ontwerp en de 
                                    realisatie van camerabewakingssystemen.
                                </p>
                                <h3>Breed inzetbaar</h3>
                                <p>     
                                De <strong>BRL Videobewakingssystemen</strong> is volgens Kiwa breed inzetbaar, zoals bij VRKI schildetectie en alarmverificatie. Maar ook bij virtuele surveillancerondes. 
                                De <strong>BRL Videobewakingssystemen</strong> is toepasbaar op openbaar gebied en privaat terrein. Voor de overgang naar de nieuwe versie van de BRL K21039/03 videobewakingssystemen
                                geldt een termijn van 1 jaar, ingegaan op de datum van publicatie zijnde 27 maart 2020. Alle overige informatie over certificering van camerabedrijven op basis van de
                                 BRL K21039 Videobewakingssystemen is te vinden op deze <a href="https://www.kiwa.com/nl/nl/service/videosystemen/" target="_blank" title="link naar kiwa website">website</a>. De <strong>beoordelingsrichtlijn</strong> is eveneens van deze website te downloaden. Voor verdere vragen kan contact
