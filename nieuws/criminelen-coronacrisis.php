                                    toegang en beveiliging. Welke organisatorische maatregelen kun je zoal in je bedrijf nemen?
                                    <ul>
                                        <li><strong>Sleutelbeheer:</strong> Voorkom dat een dief een sleutel weet te stelen. Zorg ervoor dat sleutels niet op plaatsen liggen waar inbrekers ze verwachten. 
                                            Registreer welke medewerkers een sleutel hebben en leg dit vast in een sleutelcontract. Hiermee maak je je personeel medeverantwoordelijk voor een goed sleutelbeheer. 
                                            Werk met gecertificeerde sleutels. Dit betekent dat je voor het bijmaken van een sleutel een pasje of certificaat nodig hebt.</li>
                                        <li>Voorzie ramen en deuren van <strong>inbraakwerend hang- en sluitwerk</strong>.</li>
                                        <li>Zorg voor <strong>inbraakwerende beglazing </strong>of scherm de ramen af met rolluiken of traliewerk.</li>
                                        <li>Zorg voor een opgeruimd en overzichtelijk terrein. Snoei regelmatig de beplanting om het bedrijfspand. Hiermee voorkom je dat een inbreker ongezien zijn gang kan gaan.</li>
                                        <li>Zorg voor goede <strong>beveiligingsverlichting</strong>, maak hierbij gebruik van bewegingssensoren.</li>
                                        <li>Zorg voor een <strong>goed alarmsysteem</strong> en verander de <strong>toegangscodes regelmatig</strong>. Vaak moet je een alarmsysteem met een code in- of uitschakelen. In de praktijk blijkt dat bedrijven 
                                            deze codes bijna nooit veranderen. Toch is het verstandig om dat regelmatig te doen. Bijvoorbeeld als het aantal gebruikers toeneemt of een medewerker vertrekt. 
                                            Alarmsystemen met elektronische sleutels bieden hiervoor een oplossing.</li>
                                        <li>Het komt steeds vaker voor dat <strong>particuliere beveiligingsbedrijven bedrijventerreinen en winkelgebieden bewaken </strong>. Je kunt ze inschakelen voor bijvoorbeeld observatie, 
