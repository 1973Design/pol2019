                                <h3> Winkelovervallen</h3>
                                <p>     
                                Door de coronacrisis zijn er minder winkels open en is de horeca gesloten. Neem als winkelier maatregelen om het risico op een overval te verkleinen en het overvallers zo 
                                lastig mogelijk te maken. Deze kiezen vaak een doelwit waar veel cash geld of waardevolle spullen aanwezig zijn. Zorg er dan ook voor dat er weinig te halen valt. Stimuleer 
                                pinbetalingen en voorkom dat er veel contant geld in de kassa ligt. Zorg dat je je beveiliging op orde hebt. Plaats<strong> camera’s, spiegels, alarmknoppen en beveilig 
                                diefstalgevoelige artikelen met bijvoorbeeld tag</strong>. Of verkoop kostbare artikelen alleen vanachter de kassa. Stel goede veiligheidsprocedures op en informeer je personeel 
                                hierover.
                                </p>
                                <h3> Diefstal van coronagoederen</h3>
                                <p>Populaire coronaproducten zoals mondkapjes en andere beschermingsmiddelen zijn zeer gewilde spullen om te stelen of te helen. Ook bepaalde medicijnen en onderdelen voor 
