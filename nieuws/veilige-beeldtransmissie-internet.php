                                <h2> Veilige beeldtransmissie via (draadloos) internet</h2>
                                <h3></h3>
                                <img src="../images/beveiliging/csl-draadloos-veilige-beeldtransmissie.jpg" title="veilige beeldtransmissie via draadloos internet">
                                <p>De meeste beelden van bewakingscamera’s worden tegenwoordig via internet overgedragen naar opnamesystemen of toezichtcentrales. 
                                    Daarbij lopen steeds meer installateurs tegen beperkingen van de beschikbaarheid en betrouwbaarheid van breedbandnetwerken op. Een andere uitdaging zijn 
                                    de beperkingen die beheerders van de ICT-infrastructuur opleggen.</p>
                                <p>Op plaatsen waar geen toegang tot breedbandnetwerken beschikbaar is, kan het installeren van een nieuwe verbinding extreem duur zijn en soms kan het
                                     weken duren voordat deze gerealiseerd is. Dat speelt bijvoorbeeld in sommige landelijke gebieden. 
                                     Ook gebeurt het dat breedband wel beschikbaar is, maar dat de verantwoordelijke daarvoor beperkingen oplegt, die het moeilijk of onmogelijk maken om camerasystemen te installeren.
                                     Dat laatste speelt zeker in situaties waarbij de beveiliging van het bestaande ICT-netwerk van cruciaal belang is. Het wordt dan niet geaccepteerd dat een derde partij, 
                                     de camera-installateur, apparaten op dat netwerk gaat aansluiten.
                                     Nu begrijpt de camera-installateur dat tegenwoordig ook steeds vaker, zo blijkt uit berichten op online-fora en uit artikelen waarin de eventuele gevolgen van het 
                                     ontbreken van goede beveiliging van de beeldtransmissie worden beschreven.
                                </p>
                                <h3>Essentieel belang</h3>
                                <p>Cyberaanvallen op camerasystemen komen steeds vaker voor. Het is daarom van essentieel belang dat dit risico beheerst wordt en dat op basis van de mogelijke gevolgen 
                                    effectieve cybersecurity-maatregelen worden getroffen. De camera’s maken beelden waarmee mensen kunnen worden geïdentificeerd.
                                    Dit betekent dat deze beelden onder de bescherming van de AVG vallen. Bedrijven die camerasystemen gebruiken, moeten dus voldoen aan de AVG. 
                                    Als de beeldoverdracht van camerasystemen via internet zonder enige codering plaatsvindt en dus blootgesteld wordt aan een mogelijke cyberaanval, 
                                    kan dat een inbreuk op de AVG veroorzaken. Er zijn websites waarop de beelden zijn te zien van duizenden bewakingscamera’s die eenvoudig zijn gehackt, 
                                    zonder dat de eigenaar het weet.
                                </p>
                                <h3>Oplossingen in de markt</h3>
                                <p>     
                                Het alternatief voor een bekabeld breedbandnetwerk is 4G. 4G wordt algemeen geaccepteerd als de beste manier om datapakketten voor bewakingsbeelden te verzenden. 4G is aanzienlijk sneller dan 3G en is momenteel veel efficiënter voor het verzenden van grote hoeveelheden gegevens. Bovendien kan 4G aanzienlijk goedkoper zijn in vergelijking met de installatiekosten van een vaste lijnverbinding.
                                Roaming-opties op 4G maken betrouwbare verbindingen mogelijk bij vitale toepassingen. Het hebben van een statisch IP-adres maakt ook externe toegang tot het 
                                systeem mogelijk zonder enige verstoring van de IT-infrastructuur. Door privé-APN’s in combinatie met VPN-tunnels te gebruiken, wordt de nodige bescherming 
                                geboden om end-to-end-connectiviteit te waarborgen en te voldoen aan beveiligingsstandaarden. Er zijn oplossingen op de markt die directe connectiviteit voor 
                                camerasystemen mogelijk maken.
                                </p>
                                <h4>CSL-router</h4>
                                <p>CSL biedt een ideale oplossing voor al deze uitdagingen in de vorm van de CSL Router. Deze is vooraf geconfigureerd om een snelle installatie mogelijk te maken voor verbinding en bediening. 
                                    De CSL Router biedt opties voor een enkele 4G-SIM of Dual 4G-SIM. Met de back-up-opties is ook een bekabelde breedbandverbinding met een failover 4G-verbinding betrouwbaarder te maken.</p>
                                <p>De CSL Router heeft een vooraf geconfigureerde VPN-tunnel, waardoor niet alleen de meldkamer het systeem kan bewaken, maar waardoor ook de eindgebruiker externe en beveiligde toegang heeft. 
                                    Alle communicatie is volledig (end-to-end) gecodeerd via het Gemini Global Platform. Dankzij het gebruik van privé-APN’s, verbeteren de 4G-SIM’s die bij de CSL Router worden geleverd, het beveiligingsniveau
                                     en voldoen ze aan de beveiligings- en privacycriteria van de AVG. Bovendien verminderen ze de kwetsbaarheid voor cyberaanvallen. Deze opties 
                                    genereren datapakketten die zijn afgestemd op de weergavebehoeften, zodat ze geen onverwachte extra kosten genereren. CSL Router biedt ook een vast IP-adres, waardoor toegang op
                                    afstand tot de camerasystemen binnen de beveiligingsinfrastructuur mogelijk is. En dat zonder kwetsbaarheid voor cyberaanvallen en zonder de bestaande communicatie in de client 
                                    te verstoren.</p>
                                

                                        

